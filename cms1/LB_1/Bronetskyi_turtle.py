from turtle import *

color('black', 'purple')
penup()
goto(0, 40)
pendown()
begin_fill()
circle(30)
end_fill()

penup()
goto(-50, 30)
pendown()
begin_fill()
right(90)
forward(140)
circle(50, 180)
forward(140)
left(90)
forward(100)
end_fill()

penup()
goto(-30, 0)
pendown()
color('black', 'blue')
begin_fill()
left(90)
forward(10)
left(90)
forward(60)
left(90)
forward(10)
circle(10, 90)
forward(40)
circle(10, 90)
end_fill()

penup()
goto(-30, -10)
pendown()
color('black', 'yellow')
begin_fill()
forward(10)
circle(10, 90)
forward(40)
circle(10, 90)
forward(10)
left(90)
forward(60)
end_fill()


done()